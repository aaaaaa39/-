void DrawGame(void)
{
	XMFLOAT3 pos;


#ifdef _DEBUG
	// デバッグ表示
	PrintDebugProc("ViewPortType:%d\n", g_ViewPortType_Game);

#endif

	// プレイヤー視点
	pos = GetPlayer()->pos;
	pos.y = 0.0f;			// カメラ酔いを防ぐためにクリアしている
	SetCameraAT(pos);
	SetCamera();

	switch(g_ViewPortType_Game)
	{
	case TYPE_FULL_SCREEN:
		SetViewPort(TYPE_FULL_SCREEN);
		DrawGame0();
		break;

	case TYPE_LEFT_HALF_SCREEN:
	case TYPE_RIGHT_HALF_SCREEN:
		SetViewPort(TYPE_LEFT_HALF_SCREEN);
		DrawGame0();

		// エネミー視点
		pos = GetEnemy()->pos;
		pos.y = 0.0f;
		SetCameraAT(pos);
		SetCamera();
		SetViewPort(TYPE_RIGHT_HALF_SCREEN);
		DrawGame0();
		break;

	case TYPE_UP_HALF_SCREEN:
	case TYPE_DOWN_HALF_SCREEN:
		SetViewPort(TYPE_UP_HALF_SCREEN);
		DrawGame0();

		// エネミー視点
		pos = GetEnemy()->pos;
		pos.y = 0.0f;
		SetCameraAT(pos);
		SetCamera();
		SetViewPort(TYPE_DOWN_HALF_SCREEN);
		DrawGame0();
		break;

	}

}


//=============================================================================
// 当たり判定処理
//=============================================================================
void CheckHit(void)
{
	ENEMY  *enemy  = GetEnemy();	// エネミーのポインターを初期化
	PLAYER *player = GetPlayer();	// プレイヤーのポインターを初期化
	BULLET *bullet = GetBullet();	// 弾のポインターを初期化

	player->max_damage = 6;

	// 敵とプレイヤーキャラ
	for (int i = 0; i < MAX_ENEMY; i++)
	{
		//敵の有効フラグをチェックする
		if (enemy[i].use == FALSE)
			continue;

		//BCの当たり判定
		if (CollisionBC(player->pos, enemy[i].pos, player->size, enemy[i].size))
		{
			g_Player.damageCount++;							// 攻撃回数カウント

			if (g_Player.damageCount >= g_Player.max_damage)
			{
				g_Player.damageCount = g_Player.max_damage;  // 上限を超えないようにする
				SetFade(FADE_OUT, MODE_RESULT);
				
				return;
			}

			UpdatePlayerStatus();
			ReleaseShadow(enemy[i].shadowIdx);
		}
	}
	
	// プレイヤーの弾と敵
	for (int i = 0; i < MAX_BULLET; i++)
	{
		//弾の有効フラグをチェックする
		if (bullet[i].use)
		{
			// 敵と当たってるか調べる
			for (int j = 0; j < MAX_ENEMY; j++)
			{
				//敵の有効フラグをチェックする
				if (enemy[j].use)
				{
					//BCの当たり判定
					if (CollisionBC(bullet[i].pos, enemy[j].pos, bullet[i].fWidth, enemy[j].size))
					{
						// 当たったから未使用に戻す
						bullet[i].use = FALSE;

						// 敵キャラクターは倒される
						enemy[j].use = FALSE;
						ReleaseShadow(bullet[i].shadowIdx);

						// エネミーの点滅処理開始
						enemy[j].Blinking = TRUE;		// 点滅フラグ
						enemy[j].BlinkTimer = 0.0f;		// 点滅経過時間
						enemy[j].BlinkCnt = 0;		// 点滅回数


						ReleaseShadow(enemy[j].shadowIdx);

						// スコアを足す
						AddScore(100);
					}
				}
			}
		}
	}

	/// エネミーの点滅処理
	for (int j = 0; j < MAX_ENEMY; j++)
	{
		if (enemy[j].Blinking)
		{
			const float BlinkInterval = 1.0f;		// 点滅間隔
			enemy[j].BlinkTimer += 1.0f / 60.0f;	// 60FPSの場合の時間経過

			// 点滅タイマーが経過時間を超えたらフラグ切り替え
			if (enemy[j].BlinkTimer >= BlinkInterval)
				enemy[j].BlinkTimer = 0.0f;		// タイマーリセットe
			enemy[j].use = !enemy[j].use;	// 表示と非表示の切り替え
			enemy[j].BlinkCnt++;			// 点滅回数を増やしていく
		}

		// 一定回数点滅したら完全に非表示
		if (enemy[j].BlinkCnt >= 20)
		{
			enemy[j].Blinking = FALSE;		// 点滅終了
			enemy[j].use = FALSE;			// エネミーを完全に消す
			ReleaseShadow(enemy[j].shadowIdx);
		}
	}

	// エネミーが全部死亡したら状態遷移
	int enemy_count = 0;
	for (int i = 0; i < MAX_ENEMY; i++)
	{
		if (enemy[i].use == FALSE) continue;
		enemy_count++;
	}

	// エネミーが０匹？
	if (enemy_count == 0)
	{
		SetFade(FADE_OUT, MODE_RESULT);
	}

}


